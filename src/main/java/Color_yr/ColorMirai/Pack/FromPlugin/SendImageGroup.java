package Color_yr.ColorMirai.Pack.FromPlugin;

public class SendImageGroup {
    private long id;
    private String img;

    public SendImageGroup()
    {}

    public void setId(long id) {
        this.id = id;
    }

    public void setImg(String img) {
        this.img = img;
    }

    public long getId() {
        return id;
    }

    public String getImg() {
        return img;
    }
}
